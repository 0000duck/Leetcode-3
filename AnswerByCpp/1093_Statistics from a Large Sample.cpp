class Solution {
public:
    vector<double> sampleStats(vector<int>& count) {
        // minimum, maximum, mean, median, most
        vector<double> res(5, 0);
        res[0] = -1;
        double sum = 0, iMost = 0;
        for(int i = 0; i<count.size(); i++){
            // 最小值只更新一次
            if(count[i] != 0 && res[0] == -1) res[0] = i;
            if(count[i] != 0) res[1] = i;
            // 因为数组里面的值会改变，所以需要单独一个变量来记录众数
            if(count[i] > iMost) res[4] = i, iMost = count[i];
            sum += i * count[i];
            //  partial_sum  采用部分和的思想计算中位数 和 平均数
            count[i] = count[i] + (i > 0 ? count[i-1] : 0);
        }
        // 从  res[i]  到  255   里面的值都是数据的个数， 所以可以通过这种方式来计算平均值
        res[2] = sum / count[255];
        // 通过二分查找来找中位数 [left, right)
        int left = res[0], right = res[1]+1;
        // 采用 lower_bound 的思想
        while(left < right){
            int mid = left + ((right-left) >> 1);
            if(count[mid] < count[255]/2) left = mid + 1;
            else right = mid;
        }
        // 个数为偶数个
        if((int)count[255]/2 % 2 == 0)
            while(count[right] < count[255]/2 + 1) right++;
        res[3] = (left + right) / 2.0;
        // 采用 lower_bound 函数来进行二分查找
        // 个数为奇数时   (num + 1) / 2  和  (num + 2) / 2 相等
        // res[3] = ((lower_bound(count.begin()+res[0], count.begin()+res[1]+1, (count[255]+1)/2) - count.begin()) + 
        // (lower_bound(count.begin()+res[0], count.begin()+res[1]+1, (count[255]+2)/2) - count.begin())) / 2.0;
        
        return res;
    }
};

